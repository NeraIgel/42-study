#include <iostream>
#include "HumanB.hpp"

// ************************************************************************** //
//                               Constructors                                 //
// ************************************************************************** //

HumanB::HumanB(const std::string& name)
	: _name(name), _pWeapon(NULL)
{}

// ************************************************************************** //
//                                Properties                                  //
// ************************************************************************** //

void	HumanB::setWeapon(Weapon& weapon)	{ _pWeapon = &weapon; }

// ************************************************************************** //
//                                Operations                                  //
// ************************************************************************** //

void	HumanB::attack(void) const
{
	if (!_pWeapon)
		std::cout << _name << " has no weapons" << std::endl;
	else
		std::cout << _name << " attacks with their " << _pWeapon->getType() << std::endl;
}

// ************************************************************************** //
//                              Implementation                                //
// ************************************************************************** //

HumanB::~HumanB()
{}
