#include "ft_utils.h"

int	ft_putendl_fd(char *s, int fd)
{
	if (s && fd >= 0)
	{
		if (ft_putstr_fd(s, fd) < 0)
			return (-1);
		if (write(fd, "\n", 1) < 0)
			return (-1);
		return (0);
	}
	return (-1);
}
